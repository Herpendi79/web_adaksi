                <p>Munas ini dirancang sebagai forum strategis untuk membahas Anggaran Dasar dan Anggaran Rumah Tangga (AD/ART), serta merumuskan rekomendasi kebijakan dalam meningkatkan kesejahteraan dosen ASN KemendiktiSaintek.</p>

                <h2 id="bergabung-bersama-kami">Bergabung Bersama Kami</h2>

                <p>Kami mengajak seluruh dosen ASN di bawah KemendiktiSaintek untuk bergabung dalam barisan perjuangan ini.</p>

                <p>Dengan semangat kolaboratif dan partisipatif, kita dapat mewujudkan perubahan positif bagi dunia pendidikan tinggi di Indonesia.</p>


