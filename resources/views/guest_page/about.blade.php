                <h2 id="siapa-kami">Siapa Kami</h2>

                <p>Aliansi Dosen Akademik dan Kevokasian Seluruh Indonesia (ADAKSI) adalah sebuah organisasi yang menghimpun para dosen Aparatur Sipil Negara (ASN) di bawah naungan Kementerian Pendidikan Tinggi, Sains, dan Teknologi (KemendiktiSaintek) dari seluruh Indonesia.</p>

                <p>Kami hadir sebagai wadah perjuangan kolektif untuk memperjuangkan hak-hak, kesejahteraan, dan pengakuan profesional bagi seluruh dosen ASN.</p>

                <h2 id="visi">Visi</h2>

                <p><strong>Terwujudnya organisasi profesi dosen ASN yang berdaulat dalam struktur, kritis dalam berpikir, solid dalam gerakan dan bermartabat melalui kesejahteraan.</strong></p>

                <h2 id="misi">Misi</h2>

                <ul>
                    <li>Mewujudkan struktur organisasi yang aktif, responsif, dan terdesentralisasi hingga level kampus (DPC);</li>
                    <li>Mengarusutamakan kesejahteraan dosen ASN dalam sistem kebijakan negara;</li>
                    <li>Menjadi pusat referensi nasional advokasi kebijakan pendidikan tinggi negeri;</li>
                    <li>Menyediakan layanan perlindungan profesi dan menguatkan kapasitas Dosen ASN;</li>
                    <li>Mengembangkan media perjuangan kolektif berbasis data, narasi, dan aksi.</li>
                </ul>

                <h2 id="perjuangan-kami">Perjuangan Kami</h2>

                <p>Sejak berdiri, ADAKSI telah aktif menyuarakan aspirasi dosen ASN melalui berbagai aksi dan dialog dengan pemangku kepentingan.</p>

                <p>Salah satu fokus utama kami adalah memperjuangkan pencairan tunjangan kinerja (Tukin) yang telah diamanatkan dalam peraturan perundang-undangan namun belum direalisasikan secara merata.</p>

                <p>Kami percaya bahwa keadilan dalam pemberian Tukin akan meningkatkan motivasi dan produktivitas dosen dalam menjalankan tridharma perguruan tinggi.</p>

                <h2 id="kegiatan-terkini">Kegiatan Terkini</h2>

